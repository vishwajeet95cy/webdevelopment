// Write a program to calculate the area of a rectangle using variables length and width.

function rectangleArea(length, width) {
  return length * width;
}

console.log(rectangleArea(10, 15));

// Create a program that takes a number as input and checks if it is even or odd using the modulo operator.

function isEvenOrOdd(val) {
  return val % 2 == 0 ? "Even" : "Odd";
}

console.log(isEvenOrOdd(2));
console.log(isEvenOrOdd(1));
